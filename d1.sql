1. CREATE TABLE EMP 
  (EMPNO NUMBER(4,0) NOT NULL PRIMARY KEY, 
  ENAME VARCHAR2(10), 
  JOB VARCHAR2(9), 
  MGR NUMBER(4,0), 
  HIREDATE DATE, 
  SAL NUMBER(7,2), 
  COMM NUMBER(7,2), 
  DEPTNO NUMBER(2,0), 
  );
ALTER TABLE EMP ADD FOREIGN KEY(MGR)
REFERENCES EMP(EMPNO);
ALTER TABLE EMP ADD FOREIGN KEY (DEPTNO)
REFERENCES DEPT(DEPTNO);

2. CREATE TABLE DEPT 
   (DEPTNO NUMBER(2,0) PRIMARY KEY, 
	 DNAME VARCHAR2(14), 
	 LOC VARCHAR2(13),
   );

